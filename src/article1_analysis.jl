function first_Lyapunov_quantity(J)
    A = ∂F₁_∂U_at_eq(J)
    B = -1
    C = ε
    a₂₀ = ∂²F₁_∂U²_at_eq(J)
    a₃₀ = ∂³F₁_∂U³_at_eq(J)
    q = -B*C
    return -π/(4*B*q*sqrt(q)) * (-2*A*B*(a₂₀^2) - (A^2 + B*C)*3*(-B*a₃₀))
end