Reλ₁(γ, ε) = (γ^2-4ε>0) ? 0.5*(γ+sqrt(γ^2-4ε)) : 0.5*γ
Reλ₂(γ, ε) = (γ^2-4ε>0) ? 0.5*(γ-sqrt(γ^2-4ε)) : 0.5*γ
Imλ₁(γ, ε) = (γ^2-4ε>0) ? 0 : 0.5*sqrt(4ε-γ^2)
Imλ₂(γ, ε) = (γ^2-4ε>0) ? 0 : -0.5*sqrt(4ε-γ^2)

Reλ₁(J) = Reλ₁(∂F₁_∂U_at_eq(J), ε)
Reλ₂(J) = Reλ₂(∂F₁_∂U_at_eq(J), ε)
Imλ₁(J) = Imλ₁(∂F₁_∂U_at_eq(J), ε)
Imλ₂(J) = Imλ₂(∂F₁_∂U_at_eq(J), ε)

